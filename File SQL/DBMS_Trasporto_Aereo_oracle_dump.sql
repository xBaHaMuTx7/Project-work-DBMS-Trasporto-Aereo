    -- TABELLA AEROPORTI
    CREATE TABLE AEROPORTI (
        CodiceAeroporto CHAR(3) PRIMARY KEY,
        Nome VARCHAR2(30) NOT NULL,
        Citta VARCHAR2(30) NOT NULL,
        Nazione VARCHAR2(30)
    );
    
    -- TABELLA AEROMOBILI
    CREATE TABLE AEROMOBILI (
        Matricola VARCHAR2(10) PRIMARY KEY,
        Modello VARCHAR2(25) NOT NULL,
        CapacitaTotale NUMBER(4) NOT NULL
    );
    
    -- TABELLA TRATTE
    CREATE TABLE TRATTE (
        CodiceTratta VARCHAR2(10) PRIMARY KEY,
        OrigineFK CHAR(3) NOT NULL,
        DestinazioneFK CHAR(3) NOT NULL,
        DistanzaKM NUMBER(6, 1),
        CONSTRAINT FK_Tratta_Origine FOREIGN KEY (OrigineFK) REFERENCES AEROPORTI(CodiceAeroporto),
        CONSTRAINT FK_Tratta_Destinazione FOREIGN KEY (DestinazioneFK) REFERENCES AEROPORTI(CodiceAeroporto),
        CONSTRAINT CHK_Tratta_Diversa CHECK (OrigineFK != DestinazioneFK)
    );
    
    -- TABELLA VOLI
    CREATE TABLE VOLI (
        IDVolo VARCHAR2(15) PRIMARY KEY,
        DataVolo DATE NOT NULL,
        OraPartenzaPrevista TIMESTAMP NOT NULL,
        OraArrivoPrevista TIMESTAMP NOT NULL,
        DurataVolo INTERVAL DAY TO SECOND,
        Stato VARCHAR2(20) DEFAULT 'PROGRAMMATO' CHECK (Stato IN ('PROGRAMMATO', 'DECOLLATO', 'ATTERRATO', 'CANCELLATO', 'RITARDO')),
        MatricolaAeromobileFK VARCHAR2(10) NOT NULL,
        CodiceTrattaFK VARCHAR2(10) NOT NULL,
        CONSTRAINT FK_Volo_Aeromobile FOREIGN KEY (MatricolaAeromobileFK) REFERENCES AEROMOBILI(Matricola),
        CONSTRAINT FK_Volo_Tratta FOREIGN KEY (CodiceTrattaFK) REFERENCES TRATTE(CodiceTratta),
        CONSTRAINT FK_Volo_DataTratta UNIQUE (DataVolo, CodiceTrattaFK),
        CONSTRAINT CHK_Volo_Coerenza_Orari CHECK (OraArrivoPrevista > OraPartenzaPrevista)
);
    
    -- TABELLA PASSEGGERI
    CREATE TABLE PASSEGGERI (
        CodiceFiscale CHAR(16) PRIMARY KEY,
        Nome VARCHAR2(20) NOT NULL,
        Cognome VARCHAR2(20) NOT NULL,
        DataNascita DATE,
        Email VARCHAR2(50) UNIQUE,
        Telefono VARCHAR2(20)
    );
    
    -- TABELLA CLASSI_DI_VIAGGIO
    CREATE TABLE CLASSI_DI_VIAGGIO (
        CodiceClasse CHAR(1) PRIMARY KEY CHECK (CodiceClasse IN ('E', 'B', 'F')),
        Descrizione VARCHAR2(30) NOT NULL,
        PrezzoMoltiplicatore NUMBER(3, 2) NOT NULL
    );
    
    -- TABELLA TARIFFE
    CREATE TABLE TARIFFE (
        CodiceTariffa VARCHAR2(5) PRIMARY KEY,
        NomeTariffa VARCHAR2(30) NOT NULL,
        CondizioniRimborso VARCHAR2(20) CHECK (CondizioniRimborso IN ('NESSUNO', 'PARZIALE', 'TOTALE')),
        PenalitaCambio NUMBER(5, 2)
    );
    
    -- TABELLA PRENOTAZIONI
    CREATE TABLE PRENOTAZIONI (
        IDPrenotazione VARCHAR2(20) PRIMARY KEY,
        DataPrenotazione DATE DEFAULT SYSDATE NOT NULL,
        Stato VARCHAR2(20) DEFAULT 'IN ATTESA' CHECK (Stato IN ('IN ATTESA', 'CONFERMATA', 'CANCELLATA')),
        DataScadenzaPagamento DATE,
        CodiceFiscalePasseggeroFK CHAR(16) NOT NULL,
        CONSTRAINT FK_Prenotazione_Passeggero FOREIGN KEY (CodiceFiscalePasseggeroFK) REFERENCES PASSEGGERI(CodiceFiscale)
    );
    
    -- TABELLA BIGLIETTI
    CREATE TABLE BIGLIETTI (
        IDBiglietto VARCHAR2(15) PRIMARY KEY,
        DataEmissione DATE DEFAULT SYSDATE NOT NULL,
        PrezzoFinale NUMBER(8, 2) NOT NULL,
        PostoAssegnato VARCHAR2(5),
        IDPrenotazioneFK VARCHAR2(20) NOT NULL,
        CodiceClasseFK CHAR(1) NOT NULL,
        CodiceTariffaFK VARCHAR2(5) NOT NULL,
        CONSTRAINT FK_Biglietto_Prenotazione FOREIGN KEY (IDPrenotazioneFK) REFERENCES PRENOTAZIONI(IDPrenotazione),
        CONSTRAINT FK_Biglietto_Classe FOREIGN KEY (CodiceClasseFK) REFERENCES CLASSI_DI_VIAGGIO(CodiceClasse),
        CONSTRAINT FK_Biglietto_Tariffa FOREIGN KEY (CodiceTariffaFK) REFERENCES TARIFFE(CodiceTariffa)
    );
    
    -- TABELLA DETTAGLI_BIGLIETTI_VOLI
    CREATE TABLE DETTAGLI_BIGLIETTI_VOLI (
        IDBigliettoFK VARCHAR2(25) NOT NULL,
        IDVoloFK VARCHAR2(15) NOT NULL,
        PRIMARY KEY (IDBigliettoFK, IDVoloFK),
        CONSTRAINT FK_Dettagli_Biglietto FOREIGN KEY (IDBigliettoFK) REFERENCES BIGLIETTI(IDBiglietto),
        CONSTRAINT FK_Dettagli_Volo FOREIGN KEY (IDVoloFK) REFERENCES VOLI(IDVolo)
    );
    
